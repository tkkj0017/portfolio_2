     */

    public function index(Request $request)
    {
        if($request->has('search_word')){
            $items = Item::where('item_name', 'like', '%'.$request->get('search_word').'%')->paginate(9);
        }
        else{
            $items = Item::paginate(9);
        }
        return view('item/item_list', ['items'=>$items]);
    }
    
